char hi_score_wpm;
void oled_render_wpm(void) {
  char wpm = get_current_wpm();
  oled_write(get_u8_str(wpm, '0'), false);

  if (wpm > hi_score_wpm) {
    hi_score_wpm = wpm;
  }

  if (hi_score_wpm > 0) {
    oled_write("    HI-SCORE: ", false);
    oled_write(get_u8_str(hi_score_wpm, '0'), false);
  } else {
    oled_write("    omrobbie.com", false);
  }
}

